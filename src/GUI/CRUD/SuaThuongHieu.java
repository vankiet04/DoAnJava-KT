    private void jButton1MousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MousePressed
        if (!jTextField1.getText().matches("[a-zA-Z\\s]+")) {
            JOptionPane.showMessageDialog(null, "Tên thương hiệu chỉ chứa chữ cái");
            return;
            
        }
        // TODO add your handling code here: sua thuong hieu
        String thuonghieu = jTextField1.getText();

        String tenTH = jTextField1.getText();
        if (bus.checkBrandName(tenTH)) {
            JOptionPane.showMessageDialog(null, "Tên thương hiệu đã tồn tại");
            return;
        }
        if (thuonghieu.equals("")) {
            JOptionPane.showMessageDialog(null, "Dung lượng không được để trống");
            return;
        }
        if (bus.checkBrandName(thuonghieu)) {
            JOptionPane.showMessageDialog(null, "Dung lượng đã tồn tại");
            return;
        }
        bus.editbrand(tenth, thuonghieu);
        ArrayList<DTO_Brand> list =  bus.editbrand(tenth, thuonghieu);
        qlth.filltablethuonghieu(list);
        this.dispose();
    }//GEN-LAST:event_jButton1MousePressed

