    private void jButton1MousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MousePressed
        String kichthuoc = jTextField1.getText().trim();

        // Kiểm tra tính hợp lệ của kích thước RAM
        if (!validateRam(kichthuoc)) {
            return; // Nếu không hợp lệ, thoát khỏi hàm
        }

        if (busRam.checkRAM(kichthuoc)) {
            JOptionPane.showMessageDialog(null, "RAM đã tồn tại");
            return;
        }

        int max = busRam.getMaxIDRAM();
        busRam.insert(new DTO_RamList(max + 1, kichthuoc));
        JOptionPane.showMessageDialog(null, "Thêm RAM thành công");

        qlram.listRAM = busRam.getAllData();
        qlram.filltableram(qlram.listRAM);
        this.dispose();
    }//GEN-LAST:event_jButton1MousePressed
    private boolean validateRam(String kichthuoc) {
            if (kichthuoc.isEmpty()) {
                JOptionPane.showMessageDialog(null, "Vui lòng nhập dung lượng RAM");
                return false;
            } else if (!kichthuoc.matches("\\d+")) {
                JOptionPane.showMessageDialog(null, "Phải là số");
                return false;
            } else {
                int numberValue = Integer.parseInt(kichthuoc);
                if (numberValue <= 0 || numberValue > 128 || (numberValue & (numberValue - 1)) != 0) {
                    JOptionPane.showMessageDialog(null, "Dung lượng RAM phải lớn hơn 0, không vượt quá 128 và là lũy thừa của 2");
                    return false;
                } else if (busRam.checkRAM(kichthuoc)) {
                    JOptionPane.showMessageDialog(null, "RAM đã tồn tại");
                    return false;
                }
            }
            return true;
        }
